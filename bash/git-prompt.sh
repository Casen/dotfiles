#!/bin/bash

        RED="\[\033[0;31m\]"
     YELLOW="\[\033[0;33m\]"
      GREEN="\[\033[0;32m\]"
       BLUE="\[\033[0;34m\]"
  LIGHT_RED="\[\033[1;31m\]"
LIGHT_GREEN="\[\033[1;32m\]"
     PURPLE="\[\033[1;34m\]"
    MAGENTA="\[\033[1;35m\]"
      WHITE="\[\033[1;37m\]"
 LIGHT_GRAY="\[\033[0;37m\]"
 COLOR_NONE="\[\e[0m\]"

parse_git_branch() {
  git branch 2> /dev/null | sed -e '/^[^*]/d' -e 's/* \(.*\)/ (\1)/'
}

export PS1="${MAGENTA}\u@\h:${PURPLE}\$(parse_git_branch) ${GREEN}\w ${PURPLE}\n\$ ${COLOR_NONE}"
